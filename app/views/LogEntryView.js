var LogEntryView = Parse.View.extend({

  tagName: "tr",
  template: _.template($('#log-entry-template').html()),

  initialize: function() {
    _.bindAll(this, 'render');
    this.model.bind('change', this.render);
  },

  render: function() {
    $(this.el).html(this.template(this.model.toJSON()));
    return this;
  }
});
